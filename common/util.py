def _get_proxy():
    proxy_ip = my_proxy.current_proxy_ip
    if proxy_ip is None:
        return None
    else:
        return {
            "http": f"http://{proxy_ip}"
        }


def check_response_is_ok(response=None):
    if response is None:
        return False
    if response.status_code != requests.codes.OK:
        log.error(f"status: {response.status_code}, url: {response.url}")
        return False
    return True
